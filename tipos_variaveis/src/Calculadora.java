 public class Calculadora {
    /**
     * Este método realiza a soma de dois números inteiros.
     * @param numeroUm Primeiro número a ser somado
     * @param numeroDois Segundo número a ser somado
     * @return A soma dos dois números inteiros
     */
    public int soma(int numeroUm, int numeroDois) {
        return numeroUm + numeroDois;
    }
}